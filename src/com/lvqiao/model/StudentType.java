package com.lvqiao.model;

public class StudentType {
	private int s_id;
	
	private String s_name;
	private String s_cno;
	private String s_number;
	private String s_teacher;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_cno() {
		return s_cno;
	}
	public void setS_cno(String s_cno) {
		this.s_cno = s_cno;
	}
	public String getS_number() {
		return s_number;
	}
	public void setS_number(String s_number) {
		this.s_number = s_number;
	}
	public String getS_teacher() {
		return s_teacher;
	}
	public void setS_teacher(String s_teacher) {
		this.s_teacher = s_teacher;
	}
	
}