package com.lvqiao.model;

public class ClassType {
	private int c_id;
	private String c_name;
	private String c_time;
	private String c_teacher;
	private String c_site;
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_time() {
		return c_time;
	}
	public void setC_time(String c_time) {
		this.c_time = c_time;
	}
	public String getC_teacher() {
		return c_teacher;
	}
	public void setC_teacher(String c_teacher) {
		this.c_teacher = c_teacher;
	}
	public String getC_site() {
		return c_site;
	}
	public void setC_site(String c_site) {
		this.c_site = c_site;
	}
	
	
}
